let a = 10;
//operadores relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);
//operadores de igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a === '10');// su usas la igualdad == te da true pero si preguntas si son el mismo tipo de dato=== te da false que es lo correcto
console.log(a !== '10');// aqui se pregunta si son diferentes tipos de datos y es true

